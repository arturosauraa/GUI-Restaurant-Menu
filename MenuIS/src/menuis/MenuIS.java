    private void jBAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAddActionPerformed
        // Converts every Text field,ComboBox and RadioButton into a String so it can then be inserted into the Table
        try{
        int dishNo = Integer.parseInt(jTFDishNo.getText());
        String dishName = jTFDishNa.getText();
        String category = jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        String spicinessValue = spiciness;
        int price = Integer.parseInt(jTFPrice.getText());
        Dish dish = new Dish(dishNo, price, dishName,category,spicinessValue);
        values.add(dish);
        intento();

        addToTable(dish, rowCounter);
        rowCounter++;
        
        } catch (Exception e){ 
            JOptionPane.showMessageDialog(null, "You haven't inserted enough values for the dish to be added to the table"); 
        }
    }//GEN-LAST:event_jBAddActionPerformed

