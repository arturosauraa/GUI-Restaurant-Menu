    private void jBSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSearchActionPerformed
        // TODO add your handling code here:
        try{
        List<Dish> searchList = values;
        int valueRequested = Integer.parseInt(jTFPrice2.getText());
        //se hizo para poder hacer una comparacion en el binary search porque se necesita comparar con elementos del mismo tipo(int)
        //It was done so in the binarysearch there could be a comparation of the same values(int)
        Dish searchDish = new Dish(1111, valueRequested,null,null,null);
        //The sort method sorts the list from the smallest number to the biggest
        //The next part I used the library Comparator to use the method comparing according to the price of the Object Dish
        searchList.sort(Comparator.comparing(Dish::getPrice));
        Dish sfd = searchList.get(Collections.binarySearch(searchList, searchDish, Comparator.comparing(Dish::getPrice)));
        JOptionPane.showMessageDialog(null, "The dish in the menu with a value of " + valueRequested + " is: " + sfd.getDish_name());
        } catch (Exception e){ 
            JOptionPane.showMessageDialog(null, "Sorry, there is no dish with that price"); 
        }
        
    }//GEN-LAST:event_jBSearchActionPerformed

    private void jBAvilibilityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAvilibilityActionPerformed
        // TODO add your handling code here:
       List<Dish> searchList = values;
       String catRequested = jComboBox2.getItemAt(jComboBox2.getSelectedIndex());
       String answer = "";
       for(int i = 0; i <= values.size()-1;i++){
           if(catRequested.equals(values.get(i).getCategory())){
               //Si "" no tiene una respuesta se le asigna un valor de "values-dish_name"
               //If "" doesn't have a value then it automatically assaign a value from the method "values.getDish_name"
               if("".equals(answer)){
                   answer = values.get(i).getDish_name();
                   //Si ya tiene un valor se pone una coma y se añade el siguiente valor
                   //If "" does have a value then after the first value a comma appears and the next value its addded
               }else{
                   answer = answer + ", " + values.get(i).getDish_name();
               }
               
           }
       }
       
        
        JOptionPane.showMessageDialog(null, "The category " + catRequested + " has this dishes: " + answer);
    }//GEN-LAST:event_jBAvilibilityActionPerformed

    
    
