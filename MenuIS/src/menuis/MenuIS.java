    private void addToTable(Dish dish, int row){
        jTable1.setValueAt(dish.getDish_no(),row,0);
        jTable1.setValueAt(dish.getCategory(),row,1);
        jTable1.setValueAt(dish.getDish_name(),row,2);
        jTable1.setValueAt(dish.getSpiciness(),row,3);
        jTable1.setValueAt(dish.getPrice(),row,4);
    }
